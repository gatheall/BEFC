		<h2>Welcome to the website of the Bald Eagle Flying Club, providing affordable flying to the Portland, Maine area for more than 60 years.</h2>

<p>Here you will find information on the club, our members, <a
href="/aircraft.php">our aircraft</a>, <a href="/learn2fly.php">learning
to fly</a>, and club activities.  Interested in joining the club? Please visit the
<a href="/join.php">How to Join</a> page and send us an application.  Curious
about the club or just want some more information? Visit the <a
href="/faqs.php">faqs</a> page for answers to commonly asked questions
about the club.</p>

<p><img alt="" src="/img/upload/images/4818D_sebago_1%281%29.JPG"
style="border-style:solid; border-width:1px; float:right; height:427px;
margin:8px; width:640px" /></p>

<p>Ever wanted to learn to fly? Have a look at the <a
href="/learn2fly.php">learn to fly</a> page for information on how to
get started and how the club can help!</p>

<p>The Bald Eagle Flying Club is based at the <a
href="https://www.portlandjetport.org/" target="_blank">Portland
International Jetport (KPWM)</a> in Portland, ME.  We are currently 40+
members strong and are open to new members who want to
experience the joy of affordable flying.  The club has two planes: a <a
href="/aircraft.php#4818D">1979 Cessna 172N</a>, and a recently
purchased <a href="/aircraft.php#8181N">1968 Piper Cherokee
PA-28-140</A>.  We have a couple of active flight instructors to help with
new members who want to learn how to fly and for existing pilots who
want to add a rating or for their flight review.</p>

<p>Our members keep the aircraft flying, and we are a safety and
maintenance conscious club - we want our planes in the air as often as
possible and we want them in the best possible flying shape when they
are in the air.  Our two maintenance officers, both of whom are also
board members, keep tabs on all maintenance items for the aircraft and
perform routine maintenance as required.  Safe, well maintained planes =
safe flights.  </p>

<p>Bald Eagle Flying Club members meet typically each month to go over
club-related items, maintenance squawks, and just to keep up with each
other and the exploits of our pilots and student pilots.  </p>

