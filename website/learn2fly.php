		<h2>So you want to learn to fly....</h2>

<p>Chances are, if you've stumbled onto this page, it is because you are interested in learning to fly. Perhaps you thought that the Bald Eagle Flying Club could help, and you'd be right. The club has helped many pilots earn their Private Pilot or Recreational licenses. We have several memebers who are Certified Flight Instructors, and we love to see new pilots take to the sky. <img alt="" src="/img/upload/images/Cleveland_night_flight_small.JPG" style="border-style:solid; border-width:1px; float:right; height:400px; margin:8px; width:533px" /></p>

<p>Interested? Visit the <a href="/contact.php">contact</a> page and send a message to the club.</p>

<h2>What's involved? </h2>

<p>Learning to fly is not a simple undertaking. It is many things; challenging, fun, memorable, exciting, unique.... but simple is not one of them. Flying is a heavily regulated endeavor, and there are a number of steps and requirements involved. </p>

<h3>I just want my pilot license.</h3>

<p>Okay - which one? There are several certificates that a pilot may
hold, each with different privileges.  The three most common are
<em><strong>Sport Pilot</strong></em>, <em><strong>Recreational
Pilot</strong></em>, and <strong><em>Private Pilot</em></strong>.  The
major difference between the them is that a Sport Pilot is allowed to
carry one passenger but can not fly at night and can only fly aircraft
in the "Light-sport (LSA) class", a Recreational pilot can carry one
passenger and has limitations on additional certificates, and a Private
Pilot can carry more than one passenger and can earn additional ratings
for different classes of aircraft, such as multi-engine or floatplane or
IFR (instrument) certification.  Sport Pilot certificates and
Recreational Pilot certificates also have less stringent requirements,
such as not needing to obtain a medical certificate from the FAA (Sport
Pilot) or having a lower total of hours mandated for training.  If you
don't think you would qualify for a 3rd class medical certificate from
the FAA, then chose Sport Pilot.  If you want to at any time carry more
than one passenger, chose Private Pilot.</p>

<h3>Not just about "Stick-and-rudder"</h3>

<p>Learning to fly is about so much more than, well, <em>learning to
fly</em>. Controlling the airplane is only one aspect of what it takes
to be a pilot.  Stick-and-rudder is a <em>big</em> part of flying, and
possibly the most <em>important</em> part, but if we were "flyers"
instead of "pilots" the skies would be much more disorganized and
dangerous.  To hold the title of pilot you will have to learn about
weather, using the radios, navigation, interaction with
Air-Traffic-Control (ATC), Airspace rules, aircraft systems, the engine
powering your aircraft and how to control it, flight-planning, VFR
flight, instrument flying, night flying...  The list goes on, and all
aspects are important to becoming a competent, and safe, pilot.</p>

<h3>Hours</h3>

<p>Many people have heard the "40 hour" requirement needed in order to
become a pilot, and in essence this is true; you need a MINIMUM of 40
hours of training to become either a Sport Pilot, Recreational, or a
Private Pilot.  Those hours are split between time with your instructor
and time flying solo: 20 of "dual received" time (time flying with an
instructor) is needed at a MINIMUM.  Most pilots in training need more
than the minimum 20 instructor hours and most also need more than the
minimum 10 hours flying solo.  It is up to the instructor to make sure
that you can safely pilot an aircraft, and that takes varying amounts of
time depending on how quickly you master the different aspects of
flight.  It is the same with flying solo - it is up to you <em>and your
instructor</em> to make sure that you are fully comfortable and
competent before you have the check ride with the FAA inspector.  And
speaking of the FAA....  </p>

<h3>The FAA</h3>

<p>As a student pilot, be prepared to become familiar with the FAA - the
Federal Aviation Administration.  They make the rules that we pilots
have to follow, and they will be deeply involved in your training.  You
will learn their rules and regulations, you will need their permission
to perform certain aspects of your training, and it is ultimately their
decision as to whether you will hold any sort of pilot certificate. 
</p>

<h3><img alt="" src="/img/upload/images/4818D_sebago_2.JPG"
style="border-style:solid; border-width:1px; float:left; height:323px;
margin:8px; width:502px" />Okay...  I want to get started.  Now
what?</h3>

<p>First, visit the <a href="/contact.php">contact</a>
page and send a message to the club.  We'll get you in contact with one
of our flight instructors and they can tell you more about the process. 
We love welcoming new pilots into the general aviation community, and
having them as members of the club!</p>

<h3>Is there anything I can do ahead of time? </h3>

<p>Yes, in fact, there is a lot you can do ahead of time.  As boring as
it sounds, you can start familiarizing yourself with the FAA Regulations
that you will be subject to.  The first place to start is the <a
href="https://www.faa.gov/regulations_policies/handbooks_manuals/aviation/phak/"
target="_blank">Pilot's Handbook</a>.  If you are pursuing the Private
Pilot certificate then you will need a 3rd class medical certification
from the FAA.  The certificate is good for several years (it varies
depending upon your age) and so there is no harm in getting it even
before you've started training.  You can find information on the FAA
medical requirements <a href="https://www.faa.gov/pilots/medical/"
target="_blank">here</a>.  Part of your training includes theory that
you will be tested on at an approved FAA testing center.  Commonly known
as "ground training", you can begin studying for this right away.  There
are many resources available, some expensive, other free.  The Pilot's
handbook is a good free resource, as is the <a
href="http://www.free-online-private-pilot-ground-school.com/"
target="_blank">Free Online Private Pilot Ground School</a> website. 
Most aids do have a cost involved, such as Sporty's Pilot training,
which has been popular with some of our members.  In addition to all of
these resources, Flight Simulators can help with familiarity of
procedures and cockpit instruments and even controls.  They will give
you an idea of what to expect and how to control an aircraft - but they
are not a true substitute for stick and rudder flying.  One very good
free flight simulator is <a href="http://www.flightgear.org/"
target="_blank">Flight Gear</a>.  On top of all of the training
resources, there are supplies you will need - most notably your own
aviation headset.  Headset choice is a highly personal choice - its
about comfort and features and cost - so just find one that works for
you.  The following items are recommended in addition to the
headset:</p>

<ul>
	<li>A pilot's knee-board - it will allow you to take notes while flying</li>
	<li>A sectional chart - the New York sectional chart includes KPWM and is the primary one you need</li>
	<li>The Montreal sectional chart covers the northern half of the state and may also be a good one to have</li>
	<li>A set of foggles - partial fogged glasses - these are needed for instrument training</li>
	<li>A log book is needed to log all of your flight time and track your hours. </li>
	<li>To carry all of this stuff, a flight bag.  </li>
</ul>

<h3>Okay, so how much is it going to cost me to become a pilot? </h3>

<p>This varies a lot from student to student, but a best guess of total cost is listed below. </p>

<ul>
	<li>50 hours of plane rental in <a href="./aircraft.php#8181N">N8181N</a> ($110/hr) - $5500</li>
	<li>40 hours of instructor time @ $50/hr - $2000</li>
	<li>Pilot's Headset - $300</li>
	<li>Kneeboard - $40</li>
	<li>Sectional charts - $30</li>
	<li>E6B flight calculator and plotter - $30</li>
	<li>Foggles - $25</li>
	<li>FAA 3rd class medical examination with AME - $80</li>
	<li>Study materials for Private Pilot Airplane (PAR) written test - $200</li>
	<li>Private Pilot - Airplane (PAR) written test - $155 (each attempt)</li>
	<li>FAA checkride (Private Pilot Practical test) - $300 (each attempt)</li>
</ul>

<p>Total: $8,750</p>

<p>Please understand that this is an estimate.  It is possible to
"cut-corners" a bit and not spend quite this much.  Perhaps you buy a
cheaper headset or spend less on study materials.  Maybe you only need
45 hours, and not 50.  That being said, it is also possible for it to
take much longer than 50 hours - and that can substantially increase the
cost.  How long it takes, and how much it costs, is very much related to
your ability as a student to absorb what you've learned and apply it,
safely, to your experience in the air.  </p>

