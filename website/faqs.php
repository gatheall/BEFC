		<h2>Frequently Asked Questions</h2>

<h3>How old is the club?</h3>

<p>The club was founded in 1956 and turned 60 years old in 2016. More info about the club's beginning can be found on the <a href="/history.php" target="_self">history</a> page. </p>

<h3><br />
Where is your home airport?</h3>

<p>The Bald Eagle Flying Club makes its home at the Portland International Jetport, KPWM, in Portland, ME.</p>

<h3><br />
How many planes does the club own?</h3>

<p>The club currently owns 2 planes, a 4 passenger <a href="/aircraft.php#4818D" target="_self">1979 Cessna 172N 180hp</a> and a 4 passenger <a href="/aircraft.php#8181N" target="_self">1968 Piper Cherokee PA-28-140</A>.</p>

<h3><br />
Are your planes IFR certified?</h3>

<p>Yes, both of our planes are IFR certified.</p>

<h3><br />
How easy is it to reserve a plane?</h3>

<p>Our current reservation system relies on <A HREF="https://www.flightcircle.com/">FlightCircle</A> scheduling software, which is web-based and designed for flying clubs. All of the members have access to it. It is very easy to add a reservation for the plane using a web browser. </p>

<h3><br />
Where/How often are the club meetings?</h3>

<p>Club meetings are typically monthly on the third Monday of the month. All members are welcome to attend. Board members meet an hour before the general membership to discuss club business.</p>

<h3><br />
Who are the board members?</h3>

<p>Current board members are listed on the <a href="/board.php" target="_self">Board</a> page. </p>

<h3><br />
How are the planes maintained?</h3>

<p>Normal preventative maintenance is done by the club members, led by our maintenance officers. Oil changes are done every 50 hours and members are welcome to volunteer to help with these. We also wash the planes several times a year and members are also asked to help with keeping the planes clean and waxed as well. </p>

<h3><br />
How much does it cost to fly?</h3>

<p>As of November 1, 2024, the hourly rate for N4818D (Cessna 172N) is $155/hr wet, and it's $130/hr wet for N8181N (Piper Cherokee).</p>

<h3><br />
How much does it cost to join the club?</h3>

<p>In order to join the club, there is a one-time initiation fee of $550, and $300 of club stock must be purchased at the same time. The stock is refundable upon leaving the club, but the initiation fee is not refundable. More information may be found <a href="/join.php" target="_self">here</a>.</p>

<h3><br />
Are your planes hangared? </h3>

<p>The Cessna and Piper are both tied down on the North ramp at the Jetport for the prime flying season and hangared in Lewiston during late fall and winter.</p>

<h3><br />
How fast/how far do your planes go? </h3>

<p>Our Cessna 172 (4818D) is comfortable at 110 KIAS cruise speed. It carries about 4.5 hours of fuel, giving 4818D an unrefueled range of approx. 500 NM. See the <a href="http://aircraft.php">aircraft</a> page for more specifics on each plane. </p>

<h3><br />
Who do I talk to about joining the club?</h3>

<p>Visit the <a href="/join.php">How to Join</a> page if you're ready to fill out a membership application. Or, if you're not quite ready for that, contact one of the <a href="/board.php">board</a> members directly to discuss. </p>

<h3><br />
Do I have to know someone in the club in order to join?</h3>

<p>Not at all. We welcome anyone who is interested in general aviation to contact us about joining the club. Student pilots are welcome as well. </p>

<h3><br />
Does the club have insurance coverage? </h3>

<p>Yes. All of our members are covered under the club's insurance policy. As a club member you are part-owner of both of the club planes, and you are covered under the club's policy as long as you are flying a club plane. </p>

<h3><br />
Can the club help me learn to fly?</h3>

<p>Absolutely. The club has several CFIs and we have helped many aspiring pilots obtain their Private Pilot certificate. Our CFIs may be emailed @ <a href="mailto:cfi@baldeagleflyingclub.org?subject=Request%20for%20information%20about%20becoming%20a%20student%20pilot%20with%20the%20flying%20club">cfi@baldeagleflyingclub.org</a>.</p>

<h3><br />
How much does it cost to become a pilot?</h3>

<p>This varies from student to student, but an example breakdown of costs may be found on the <a href="/learn2fly.php" target="_self">learning to fly</a> page. On average it is about $10,000, but that price can be much higher if you require more hours before you are ready for your checkride. </p>

