<p><img alt="" src="/img/upload/images/18D%20panel.jpg"
style="border-style:solid; border-width:1px; float:left; height:371px;
margin:8px; width:660px" /></p>

<p>In 2019, N4818D emerged from the avionics shop with a brand-new
Skyview HDX panel, complete with 2 axis autopilot and Garmin GTN650!
Both the GTN and Dynon PFD are touch screens, and the Dynon PFD and EFIS
display include backup batteries for redundancy.  The autopilot has
dedicated controls with a "level" button and knobs for altitude bug,
heading bug, and baro settings.  The panel upgrade included a revamp of
all of the old fuses and a brand new laser-cut panel!</p>

<p>Lastly, N4818D flew down to Three in the Green in Selingsgrove PA in
January 2020, from which it emerged several months later with a fantastic,
new paint scheme.</p>

<p><br />
<strong>Other upgrades include:</strong></p>

<ul>
	<li>Horton STOL Kit</li>
	<li>Tanis Heater</li>
	<li>Rosen Sun Visors</li>
	<li>Wing-tip Strobes</li>
	<li>Powerflow Exhaust</li>
	<li>IFR Certified</li>
</ul>

