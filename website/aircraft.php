<h2><a id="4818D" name="4818D">1979 Cessna 172N</a></h2>

<p>The Club's 1979 Cessna 172N, tail number N4818D, has been upgraded
with the <a
href="https://www.pennyanaero.com/stcs-hp-upgrades.php">Superhawk STC
180-hp Penn-Yan</a> conversion, two-axis autopilot, multi-function
display, IFR GPS, and STOL kit.  For members, the Weight and Balance
calculator for 18-Delta may be found in the club's Google Drive.  N4818D
also had its interior completely re-done in December of 2016 by Tam's
Upholstery Touch and the results speak for themselves!</p>

