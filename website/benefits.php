		<h2>Benefits of Club Membership</h2>

<p>If you've stumbled upon this page, you're probably wondering why you
would want to join the club; what's in it for you.  There must be a
benefit here, right? Read below for some of the best reasons that club
membership might be for you.</p>

<h3>You get to own an airplane<img alt=""
src="/img/upload/images/4818D_right_1.jpg" style="border-style:solid;
border-width:1px; float:right; height:450px; margin:8px; width:600px"
/></h3>

<p>And not just one airplane, but two! Every club member buys a share
into the club, and that share makes them part-owner of all aircraft 
the club owns.  For many of us, this is the closest we will ever come to
owning an aircraft on our own.  Let's face it, between purchase costs,
maintenance costs, hanger and tie-down fees, overhauls, etc.... 
airplanes are expensive.  Many people never get to experience airplane
ownership or the joy of having a set of keys to an aircraft. Through
club membership, you can.</p>

<h3>Familiarity with the planes you fly</h3>

<p>For those of us who have had experience with renting a plane, you
know the minor frustration of getting into an airplane that you've never
even sat in before and trying to make a flight.  As part-owners of the
club's aircraft, you have the chance to fly the same plane whenever
it is available.  This builds familiarity and comfort with the aircraft. 
You learn how it flys, how it feels, what it likes and doesn't like, and
there is something to be said for that.</p>

<h3>Insurance coverage</h3>

<p>Insurance in general is expensive, and aviation liability insurance
is no different.  As a club member, you are covered as a pilot for one
million dollars of liability and full hull coverage as a named insured
under the club's insurance policy.  This coverage begins automatically
once you are checked out in our planes by one of the club's instructors. 
The coverage is <em>only</em> good if you are flying one of the club's
planes; if you rent another aircraft, you <em>will</em> need your own
coverage for that flight.</p>

<h3><img alt="" src="/img/upload/images/4818D_right_1.jpg"
style="border-style:solid; border-width:1px; float:left; height:400px;
margin:8px; width:683px" />Cost</h3>

<p>Aircraft usage fees for our aircraft are very competitive compared to
normal aircraft rental fees, and are a big benefit to our members.  Our
Cessna 172N, N4818D is $130/hr, and our Piper Cherokee, N8181N, is
$110/hr.  All usage fees are
"wet" -- meaning they include fuel -- and time is measured by tach time,
which is more "pilot friendly" than Hobbs time.</p>

<h3>Use the plane whenever you want, for as long as you want (within
reason)</h3>

<p>As part-owner of the airplane, you can use the airplane whenever you
want, and for as long as you want - within reason of course. You only
pay for the time the engine is turning - tach time.  If you
want to take the plane for a day trip or even a weekend, go ahead!
It's your plane! Take the wife on a weekend trip to Martha's
Vineyard, or take the whole family for a vacation to Ohio.</p>

<p>Interested in becoming a member? Fill out a <a href="/join.php">membership application</a>!</p>

