		<h3>Did you know?</h3>

<p>The Bald Eagle Flying Club has had more than two hundred members in
its 50+ year history, and has owned 10 different aircraft.  The club has
always considered Portland International Jetport its home but has also
wintered its planes at the Biddeford Municipal Airport, the Brunswick
Executive Airport and the Auburn/Lewiston Municipal airport.</p>
 
