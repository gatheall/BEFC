		<h1>The History of the Bald Eagle Flying Club</h1>

<h3>The Beginning</h3>

<p>It began as a simple idea shared between four pilots in the
mid-nineteen fifties; why not stop renting airplanes and buy one of our
own? The airplane in question, a 1940's era Aeronca Champ, was purchased
shortly after the four men, Fred Rozzi, Sam Mangino, Andy Mavrakos, and
Enrico Peruzzi, saved the $400 needed for the downpayment on the plane
and obtained a $400 mortgage from Casco Bank to cover the remaining
cost.<a
href="https://commons.wikimedia.org/wiki/File:Municipal_Airport,_Portland,_Maine_(76929).jpg#/media/File:Municipal_Airport,_Por
target="_blank"><img alt=""
src="/img/upload/images/Municipal_Airport%2C_Portland%2C_Maine_ext_small.jpg"
style="border-style:solid; border-width:1px; float:right; height:193px;
margin:8px; width:300px" /></a></p>

<p>Humble beginnings for a club that has lasted more than
half a century.</p>

<p><a href="/img/upload/files/N3935S_1.png" target="_blank"><img alt=""
src="/img/upload/images/N3935S_1_ext_small.png"
style="border-style:solid; border-width:1px; float:left; height:209px;
margin:8px; width:300px" /></a> From what we know, the founding members
flew that little plane as often as they could manage.  None of them or
those who joined after were wealthy men, but the costs of flying then
were not what they are today, and neither were the rules.  Original
member Fred Rozzi once flew the little Champ <em>under</em> the old
Million Dollar Bridge, a feat for which he was verbally reprimanded by
the FAA, being asked politely to "not do it again".  There were other
incidents as well: The Aeronca Champ, being without an electric starter,
was once crashed into another plane on the ramp outside Maine Aviation,
the pilot having forgotten to set the parking break while hand-proping
it. <a
href="https://100thbg.com/index.php?option=com_bombgrp&view=personnel&id=5601&Itemid=334"
target="_blank">Harry B.  "The Colonel" Winger</a> was instrumental in
helping the club in those early days.  A former B-17 pilot and member of
the 100th Bomber Group, Harry was Director of Flight Operations for
Maine Aviation and did what he could to see that the club members had
what they needed to park and fly the Champ as often as possible.</p>

<h3>The Sixties</h3>

<p><a href="/img/upload/files/N3935S_2.png" target="_blank"><img alt=""
src="/img/upload/images/N3935S_2_ext_small.png"
style="border-style:solid; border-width:1px; float:right; height:223px;
margin:8px; width:300px" /></a>From its initial four members and single
two-seat fabric covered airplane the club grew, and by the late nineteen
sixties had sold the Aeronca Champ and upgraded to a four-seat 1963
Cessna 172 Skyhawk, tail number<a
href="http://registry.faa.gov/aircraftinquiry/NNum_Results.aspx?nNumberTxt=3935S"
target="_blank">N3935S</a> - a plane that was eventually sold but is
still flying today in the skies over Texas.  That plane is also believed
to have been involved in search-and-rescue operations with club member
Bob Philbrook at the controls.  Mr.  Philbrook was a member of the Civil
Air Patrol and a sufferer of Multiple Sclerosis, but once wedged into
the cockpit was as competent as any pilot the club has ever included in
its ranks.</p>

<h3>The Seventies</h3>

<p>The growth and change continued, and by the mid nineteen seventies
the club had over thirty members, including several women pilots, and
two airplanes, a 1970 Cessna 172 Skyhawk,<a
href="http://registry.faa.gov/aircraftinquiry/NNum_Results.aspx?nNumberTxt=7813G"
target="_blank">N7813G</a>, and a Cessna 150 Commuter, remembered by 
Mark Chapman as tail number 'four-four-juliet'. 
The <a href="/img/upload/files/N7813G_1.png" target="_blank"><img alt=""
src="/img/upload/images/N7813G_1_ext_small.png"
style="border-style:solid; border-width:1px; float:left; height:213px;
margin:8px; width:300px" /></a> Skyhawk and the Commuter were staples for
the club in the 1970's, and those planes and the club's size and
prosperity prompted a <a href="/img/upload/files/article.png"
target="_blank">newspaper article</a> to be written about the club,
which has become a partial source for the information found on this
page.  As far as we (and the FAA) know, N7813G is still flying today,
albeit under different ownership, gracing the skies of Hawaii for Maui
Flyers, Inc.  Current usage rates at that time were $7 an hour for
N7813G, and $4.50 an hour for the Cessna 150, both of those being "dry"
rates - fuel was paid for by the members as they flew.  Membership cost
was $10 a month.</p>

<h3>The Eighties and Nineties</h3>

<p>The history of the club becomes a bit vague at this point.  We don't
know much of the activities of the members during the late seventies and
eighties, but we do know of the planes that the club owned
during that time.  After N7813G and 44J came another Cessan 172, this a
1975 model, tail number N6564H.  This plane sadly suffered the worst
accident in the history of the club - a <a
href="http://aviation-safety.net/wikibase/wiki.php?id=37756"
target="_blank">fatal flight</a> with a club member at the controls -
the bigger tragedy of course being the loss of the three souls on board
the aircraft. <a href="/img/upload/files/N52698_and_N73827.png"
target="_blank"><img alt=""
src="/img/upload/images/N52698_and_N73827_ext_small.png"
style="border-style:solid; border-width:1px; float:right; height:207px;
margin:8px; width:300px" /></a></p>

<p>Despite the accident, the club continued on, and eventually bought two
airplanes to replace N6564H, a 1976 Cessna 172N, tail number <a
href="http://registry.faa.gov/aircraftinquiry/NNum_Results.aspx?nNumberTxt=73827"
target="_blank">N73827</a>, and a 1973 Cessna 182P, tail number <a
href="http://registry.faa.gov/aircraftinquiry/NNum_Results.aspx?nNumberTxt=52698"
target="_blank">N52698</a>.  The club owned and flew these two planes
during the late nineties and into the new century, a time period that
includes another minor tragedy - a fire at then club treasurer Peter
Holden's house that destroyed many of the records for the club.</p>

<h3>The Day the Club Almost Dissolved</h3>

<p><a href="/img/upload/files/N73827_3.png" target="_blank"><img alt=""
src="/img/upload/images/N73827_3_ext_small.png"
style="border-style:solid; border-width:1px; float:left; height:199px;
margin:8px; width:300px" /></a> The middle of the first decade of the new
century was a hard time for the club.  Membership began to dwindle, the
economic downturn and the events of September the 11th, 2001 combining
to make general aviation a luxury that most of the members could no
longer afford.  In 2002, N52698 was sold to former member Tom Marshall,
the club membership no longer being able to justify the expense of
owning two airplanes.  2008 saw the ranks shrink to just six
members, former club President Luke Bassett, former club Vice President
Jay Crosby, former Treasurer Dave Hempstead, longest tenured member and
former board member Mark Chapman, and two others.  There were serious
talks during those times of selling the one remaining plane, N73827, and
dissolving the club, the six members having to bear more financial
burden than they could reasonably justify.</p>

<h3>The Current Era<a href="/img/upload/files/N52698_3.png"
target="_blank"><img alt=""
src="/img/upload/images/N52698_3_ext_small.png"
style="border-style:solid; border-width:1px; float:right; height:199px;
margin:8px; width:300px" /></a></h3>

<p>Thankfully the club endured and in 2009 welcomed two new members,
former Maintenance Officer and Club President Jim Stenberg and
former Maintenance Officer Jason Barnes, bringing the number of members to
eight and tabling the decision to sell N73827 for the time being.  By
2012 the membership had grown to twelve, and the club was part of
another <a
href="http://www.pressherald.com/2012/07/03/fewer-taking-wing-as-small-plane-pilots_2012-07-03/"
target="_blank">newspaper article by the Portland Press Herald</a>. 
Events in late 2013 would further swell the club's ranks, as Maine
Aviation decided to close their Pilot Training program <a
href="http://www.maineaviation.com/flight.html"
target="_blank">effective January 1st, 2014</a>.  Many of those current
students finished their training as members of the club, continuing to
work with CFI Chip Lawrence, also formerly of Maine Aviation.  In the winter of 2013,
N73827 was indeed sold, but only because it was time to replace it. 
March of that same year saw the club purchase its current Cessna 172, an
'N' model from 1979 with an upgraded powerplant, tail number <a
href="/aircraft.php#4818D" target="_blank">N4818D</a>.  An upturn in
the economy saw the membership continue to grow, and in early 2015 a
second airplane was again needed to keep pace with the flying habits of
the current members. N239AM, a 2004 Alarus CH2000 trainer, was purchased
and was soon racking up hours with many of the club's student
pilots flying it weekly.  2016 saw the club's ranks swell past 50
members, and February of 2017 saw the club grow their fleet to three
planes with the purchase of a 1985 Cessna 172P model, tail number N99265.</p>

<p>In late 2018, the club purchased a two-seat light sport airplane, a
Vans RV-12, tail number N4647R, to replace the Alarus, which had been
involved in an accident earlier that year.  The following year, the club
sold N99265 due largely to a fall in membership.  Finally, the club sold
N4647R late in 2020 and replaced it several months later with a Piper
Cherokee PA-28-140, tail number N8181N. 



<h3>What the Future May Hold</h3>

<p>Times have certainly changed since the club was founded.  Thanks to
increased airport security, members now need special security badges to
operate at their main home at the Portland International Jetport. 
Northeast Aviation, formerly Northeast Airmotive, is the current FBO
working with the club for hangar and tie-down space and small
maintenance items, and for the past few years the club has even afforded
to be able to hangar the planes during the winter, erasing the
back-breaking chore of sweeping snow and chipping ice off of the wings
and fuselage after each snowfall.  Snow cleanup was an ongoing winter
event that had been with the club since its inception and one that
former member and Operations Office Dwight Woodman came to dread.  So
what's next for this little flying club of ours?</p>

<p>At this point in time the club is as alive and active, and we hope
the history continues for years to come.  2021 marks the 65th year for
the club, and who's to say another 65 aren't in the making!</p>

